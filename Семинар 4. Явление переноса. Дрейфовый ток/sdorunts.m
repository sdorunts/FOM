q           = 1.6*1e-19;    % заряд электрона, Кл
n           = 1.6*1e10;     % собственная концентрация носителей, см^-1
T1          = 100;          % первая температура, К
T2          = 265;          % вторая температура, К

MnSiT1      = 22576;        % подвижность электронов в Si при Т1, см^2/В·с
MpSiT1      = 4098;         % подвижность дырок в Si при T1, см^2/В·с
nSiT1       = 1.65e-09;     % собственная концентрация носителей в Si при Т1, см^-3
MnSiT2      = 5188;         % подвижность электронов в Si при T2, см^2/В·с
