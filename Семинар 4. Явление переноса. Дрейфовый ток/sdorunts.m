MnGeT2      = 9566;         % подвижность электронов в Ge при T2, см^2/В·с
MpGeT2      = 7880;         % подвижность дырок в Ge при T2, см^2/В·с
nGeT2       = 2.11e+13;     % собственная концентрация носителей в Ge при Т2, см^-3

MnGaAsT1    = 269935;       % подвижность электронов в GaAs при Т1, см^2/В·с
